export type RegionAvailability = {
  code: string;
  name: string;
  status: 'published' | 'scheduled' | 'region_restricted' | 'blocked_by_admin';
  scheduledAt: string | null;
};

export function getAllRegionsSameState(status: 'published' | 'scheduled' | 'region_restricted' | 'blocked_by_admin' = 'published'): RegionAvailability[] {
  return Object.entries(countryNamesES).map(([code, name]) => ({
    code: code.toUpperCase(),
    name,
    status,
    scheduledAt: null,
  }));
}


